public class Alert {
    String atitle;
    String amsg;
    String aicon;
    String link;

    public String getAtitle() {
        return atitle;
    }

    public void setAtitle(String atitle) {
        this.atitle = atitle;
    }

    public String getAmsg() {
        return amsg;
    }

    public void setAmsg(String amsg) {
        this.amsg = amsg;
    }

    public String getAicon() {
        return aicon;
    }

    public void setAicon(String aicon) {
        this.aicon = aicon;
    }

    public String getLink() {
        return link;
    }

    public void setLink(String link) {
        this.link = link;
    }
}
